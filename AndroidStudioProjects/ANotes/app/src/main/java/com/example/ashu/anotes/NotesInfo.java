package com.example.ashu.anotes;

public class NotesInfo {
    String mTitle,mContent;

    public NotesInfo(String mTitle, String mContent) {
        this.mTitle = mTitle;
        this.mContent = mContent;
    }

    public String getmTitle() {
        return mTitle;
    }

    public String getmContent() {
        return mContent;
    }
}
